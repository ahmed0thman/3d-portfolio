export const calculateSizes = (isSmall, isMobile, isTablet) => {
  return {
    deskScale: isSmall ? 0.04 : isMobile ? 0.05 : 0.065,
    deskPosition: isSmall
      ? [0.5, -3.5, 0]
      : isMobile
      ? [0.5, -4, 0]
      : [0.25, -6.5, 0],
    cubePosition: isSmall
      ? [2.3, -6, 0]
      : isMobile
      ? [3.5, -6, 0]
      : isTablet
      ? [5.5, -6, 0]
      : [9, -5.5, 0],
    reactLogoPosition: isSmall
      ? [2.5, 2.5, 0]
      : isMobile
      ? [3.4, 4, 0]
      : isTablet
      ? [5.5, 3, 0]
      : [9, 4, 0],
    ringPosition: isSmall
      ? [-3, 2.5, 0]
      : isMobile
      ? [-4, 3, 0]
      : isTablet
      ? [-6.5, 3, 0]
      : [-8.5, 3, 0],
    targetPosition: isSmall
      ? [-3.8, -10, -10]
      : isMobile
      ? [-6, -10, -10]
      : isTablet
      ? [-8, -11, -10]
      : [-12, -11, -10],
  };
};

